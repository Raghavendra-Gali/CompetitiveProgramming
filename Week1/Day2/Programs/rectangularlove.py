def find_rectangular_overlap(rect1, rect2):

    # Calculate the overlap between the two rectangles
    if rect1['left_x']>rect2['left_x']:
    	rx1=rect1['left_x']
    else:
    	rx1=rect1['left_x']

    if rect1['bottom_y']>rect2['bottom_y']:
    	rc1=rect1['bottom_y']
    else:
    	rc1=rect1['bottom_y']

    if rect1['left_x']+rect1['width']<rect2['left_x']+rect2['width']:
    	rx2=rect1['left_x']+rect1['width']
    else:
    	rx2=rect2['left_x']+rect2['width']
    

    rx1 = max(rect1['left_x'], rect2['left_x'])
    
    rc1 = max(rect1['bottom_y'], rect2['bottom_y'])
    
    rx2 = min(rect1['left_x'] + rect1['width'], rect2['left_x'] + rect2['width'])
    
    rc2 = min(rect1['bottom_y'] + rect1['height'], rect2['bottom_y'] + rect2['height'])
    
    width = rx2 - rx1
    heigth = rc2 - rc1
    
    if width <= 0 or heigth <= 0:
        return {'left_x': None, 'bottom_y': None, 'width': None, 'height': None}
    return {'left_x': rx1, 'bottom_y': rc1, 'width': width, 'height': heigth}
    

